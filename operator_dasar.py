a = 10
b = 20

# aritmatika
print(a+b)  # penjumlahan
print(a-b)  # pengurangan
print(a*b)  # perkalian
print(a/b)  # pembagian
print(a//b)  # pembagian bulat
print(a%b)  # sisa bagi
print(a**b)  # perpangkatan
# penugasan 
a += 5  # a = a + 5
print(a)  # 15 
b -= 5  # b = b - 5
print(b)  # 15
# pembanding    
print(a == b)  # sama dengan
print(a != b)  # tidak sama dengan
print(a > b)  # lebih besar dari
print(a < b)  # lebih kecil dari
print(a >= b)  # lebih besar dari atau sama dengan
print(a <= b)  # lebih kecil dari atau sama dengan
# logika
print(a > 5 and b < 20)  # dan
print(a > 5 or b < 20)  # atau
print(not(a > 5))  # tidak    